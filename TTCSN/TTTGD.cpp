#include <iostream>
#include <vector>

using namespace std;


void contractVertex(vector<vector<int>>& graph, int u, int v) {
    int n = graph.size();
    for (int i = 0; i < n; i++) {
        if (i != u && i != v) {
            graph[u][i] += graph[v][i];
            graph[i][u] += graph[i][v];
        }
    }
    
    graph.erase(graph.begin() + v);
    for (int i = 0; i < n - 1; i++) {
        graph[i].erase(graph[i].begin() + v);
    }
}


void printGraph(const vector<vector<int>>& graph) {
    int n = graph.size();
    
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << graph[i][j] << " ";
        }
        cout << endl;
    }
}


vector<vector<int>> inputGraph() {
    int n;
    cout << "Enter the number of vertices in the graph: ";
    cin >> n;
    
    vector<vector<int>> graph(n, vector<int>(n, 0));
    
    cout << "Enter the adjacency matrix of the graph:" << endl;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> graph[i][j];
        }
    }
    
    return graph;
}

int main() {

    vector<vector<int>> graph = inputGraph();
    
    int u, v;
    cout << "Enter the vertices to contract: ";
    cin >> u >> v;
    
    cout << "Graph before contraction:" << endl;
    printGraph(graph);
    
    contractVertex(graph, u, v);
    
    cout << "Graph after contraction:" << endl;
    printGraph(graph);
    
    return 0;
}

